"""
    Una vez que inicializamos una variable, podemos modificar su valor en cualquier momento.
    Esto significa que podemos cambiar el valor de una variable en cualquier momento del programa.
    Para modificar el valor de una variable, simplemente asignamos un nuevo valor a la variable.
    Con escribir de nuevo el nombre de la variable y el nuevo valor, es suficiente.

"""

#Declarar e inicializar una variable
nombre_1 = "Juan"
genero_1 = "Masculino"
edad_1 = 30

#Imprimir el valor de la variable
print(f"Hola, soy {nombre_1}, tengo {edad_1} años y soy {genero_1}") #"Hola, soy Juan, tengo 30 años y soy Masculino"

#Modificar el valor de la variable
nombre_1 = "Pedro"
edad_1 = 25

#Imprimir el valor de la variable
print(f"Hola, soy {nombre_1}, tengo {edad_1} años y soy {genero_1}") #"Hola, soy Pedro, tengo 25 años y soy Masculino"

