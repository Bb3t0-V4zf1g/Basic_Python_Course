"""
    TIPOS DE DATOS BÁSICOS EN PYTHON

    Python tiene varios tipos de datos integrados. A continuación, veremos los 4 más comunes:

    str: cadenas de texto (string)
    int: números enteros
    float: números decimales
    bool: valores booleanos (True o False)
"""

# str: texto entre comillas
nombre = "Lucía"
ciudad = 'Buenos Aires'

# int: números enteros
edad = 28
ano_nacimiento = 1995

# float: números decimales
altura = 1.65
pi = 3.1416

# bool: verdadero o falso
es_mayor = True
tiene_mascota = False

# Mostrar los valores y sus tipos
print(nombre, type(nombre))
print(edad, type(edad))
print(altura, type(altura))
print(es_mayor, type(es_mayor))

