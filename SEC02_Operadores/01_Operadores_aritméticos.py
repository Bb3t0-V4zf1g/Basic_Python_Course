"""
    OPERADORES ARITMÉTICOS

    +   Suma
    -   Resta
    *   Multiplicación
    /   División (resultado float)
    //  División entera (floor)
    %   Módulo (resto)
    **  Potencia
"""

# Ejemplos
a = 10
b = 3
print("Suma:", a + b)          # 13
print("Resta:", a - b)         # 7
print("Multiplicación:", a * b) # 30
print("División:", a / b)       # 3.333...
print("División entera:", a // b) # 3
print("Módulo:", a % b)         # 1
print("Potencia:", a ** b)      # 1000

"""
    El módulo (%) devuelve el resto de la división.
    Por ejemplo, 10 % 3 = 1, porque 10 = 3 * 3 + 1.
"""

